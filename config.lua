lvim.builtin.which_key.mappings["lh"] = {
  function()
    local buf = vim.api.nvim_get_current_buf()
    local ft = vim.api.nvim_buf_get_option(buf, "filetype")
    if ft == "c" or ft == "cpp" then
      vim.cmd [[ClangdSwitchSourceHeader]]
    end
  end,
  "Go To Header",
}
